package org.lecturestudio.web.api.http;

import java.net.HttpURLConnection;
import java.net.URL;
import java.net.URLEncoder;
import java.nio.charset.StandardCharsets;
import java.util.Map;

public class HttpClient {

	private final URL url;


	public HttpClient(URL url) {
		this.url = url;
	}

	public int post(Map<String, String> parameters) throws Exception {
		String params = getParamsString(parameters);

		byte[] payload = params.getBytes(StandardCharsets.UTF_8);

		HttpURLConnection con = (HttpURLConnection) url.openConnection();
		con.setRequestMethod("POST");
		con.setRequestProperty("Content-Type", "application/x-www-form-urlencoded");
		con.setRequestProperty("Content-Length", Integer.toString(payload.length));
		con.setDoOutput(true);
		con.getOutputStream().write(payload);
		con.getOutputStream().flush();

		return con.getResponseCode();
	}



	private static String getParamsString(Map<String, String> params) throws Exception {
		StringBuilder result = new StringBuilder();

		for (Map.Entry<String, String> entry : params.entrySet()) {
			result.append(URLEncoder.encode(entry.getKey(), "UTF-8"));
			result.append("=");
			result.append(URLEncoder.encode(entry.getValue(), "UTF-8"));
			result.append("&");
		}

		String resultString = result.toString();
		return resultString.length() > 0 ? resultString.substring(0, resultString.length() - 1) : resultString;
	}
}
